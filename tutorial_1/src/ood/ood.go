package main

/* OOD = Object Oriented Design , Object 중심의 설계
OOD 설계 방법 S.O.L.I.D
S - Single responsbility principle	***
O - Open/closed principle			***
L - Liskov substitution principle
I - Interface segregation principle
D - Dependency inversion principle

의존성을 낮추고
응집성을 높힌다
*/

/*
S - Single responsbility principle
단일 책임 원칙
하나의 객체는 하나의 책임을 가져야 한다
*/

/*
O - Open/closed principle
확장에는 열려 있고, 변경에는 닫혀 있다
*/

/*
L - Liskov substitution principle
리스코프 치환 이론
BaseType의 기존 함수,동작을 바꾸지 마라
(Golang에서는 상속지원하지 않기에 상관은 없다. interface만 지원)
*/

/*
I - Interface segregation principle
여러개의 관계를 모아놓은 인터페이스 보다,
관계 하나씩 정의하는게 더 좋다
*/

/*
D - Dependency inversion principle
관계는 인터페이스에 의존해야지
객체에 의존하면 안된다
*/
